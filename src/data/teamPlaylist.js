export function getRandomArtist() {
  const idx = Math.floor(Math.random() * teamArtists.length)
  return teamArtists[idx]
}

export function getRandomGrammarExercise() {
  const idx = Math.floor(Math.random() * grammarExercises.length)
  return grammarExercises[idx]
}

export function getRandomConversationPrompt() {
  const idx = Math.floor(Math.random() * conversationPrompts.length)
  return conversationPrompts[idx]
}

export function getArtistByName(name) {
  return teamArtists.find(a => a.name.toLowerCase() === name.toLowerCase()) || null
}
